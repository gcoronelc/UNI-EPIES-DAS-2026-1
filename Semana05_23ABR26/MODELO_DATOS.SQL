
CREATE TABLE ELECCIONES
( 
	ID_ELECCION          integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(150)  NOT NULL ,
	ESTADO               integer  NOT NULL ,
	CONSTRAINT XPKELECCIONES PRIMARY KEY (ID_ELECCION ASC)
)
go



CREATE TABLE PARTIDO_POLITICO
( 
	ID_PARTIDO           integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(150)  NOT NULL ,
	CONSTRAINT XPKPARTIDO_POLITICO PRIMARY KEY (ID_PARTIDO ASC)
)
go



CREATE TABLE EMPLEADO
( 
	ID_EMPLEADO          integer IDENTITY ( 1,1 ) ,
	DNI                  varchar(20)  NOT NULL ,
	APELLIDOS            varchar(100)  NOT NULL ,
	NOMBRES              varchar(100)  NOT NULL ,
	USUARIO              varchar(20)  NULL ,
	CLAVE                varchar(20)  NULL ,
	CONSTRAINT XPKEMPLEADO PRIMARY KEY (ID_EMPLEADO ASC)
)
go



CREATE TABLE REGISTRO
( 
	ID_ELECCION          integer  NOT NULL ,
	ID_PARTIDO           integer  NOT NULL ,
	FECHA                datetime  NOT NULL ,
	HORA                 varchar(20)  NOT NULL ,
	OBSERVACIONES        text  NULL ,
	ID_EMPLEADO          integer  NOT NULL ,
	CONSTRAINT XPKREGISTRO PRIMARY KEY (ID_ELECCION ASC,ID_PARTIDO ASC),
	CONSTRAINT R_1 FOREIGN KEY (ID_ELECCION) REFERENCES ELECCIONES(ID_ELECCION)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT R_2 FOREIGN KEY (ID_PARTIDO) REFERENCES PARTIDO_POLITICO(ID_PARTIDO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT R_4 FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO(ID_EMPLEADO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE CANDIDATOS
( 
	ID_CANDIDATO         integer IDENTITY ( 1,1 ) ,
	ID_ELECCION          integer  NOT NULL ,
	ID_PARTIDO           integer  NOT NULL ,
	DNI                  varchar(20)  NOT NULL ,
	APELLIDOS            varchar(100)  NOT NULL ,
	NOMBRES              varchar(100)  NOT NULL ,
	FEC_NAC              datetime  NOT NULL ,
	ORDEN                integer  NOT NULL ,
	CONSTRAINT XPKCANDIDATOS PRIMARY KEY (ID_CANDIDATO ASC),
	CONSTRAINT R_3 FOREIGN KEY (ID_ELECCION,ID_PARTIDO) REFERENCES REGISTRO(ID_ELECCION,ID_PARTIDO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


